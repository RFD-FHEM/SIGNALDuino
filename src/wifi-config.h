void handleFavicon() {
  // bei 2 mal sendHeader ganz schechter Empfang von SD_WS_117_Bresser_7in1, bei einmal sendHeader OK egal welcher ???
  wifiManager.server->sendHeader(F("cache-control"), F("max-age=31536000, immutable")); // Cache-Control: max-age=31536000, immutable (1 Jahr)
  // wifiManager.server->sendHeader(F("x-content-type-options"), F("nosniff"));            // Response should include 'x-content-type-options' header.
  wifiManager.server->send_P(200, "image/x-icon", tblFavicon, sizeof(tblFavicon));
}

void handleSignal() {
  String page;
  page.reserve(4096);
  page += FPSTR(HTTP_HEAD_START);
  page.replace(FPSTR(T_v), F("SIGNAL-ESP"));
  page += FPSTR(HTTP_STYLE);
  page += FPSTR(HTTP_HEAD_END);
  if (EEPROM.read(EE_WM_THEME)) { // theme dark
    page.replace(FPSTR(T_c), F("invert")); // add class str for dark mode
  }
  page += F("<h3>SIGNAL-ESP</h3><hr><dl>");
  page += F("<dt>Firmware-Version</dt><dd>");
  page += PROGVERS;
  page += F("</dd>");
  page += F("<dt>SDK-Version</dt><dd>");
  page += ESP.getSdkVersion();
  page += F("</dd>");
#ifdef ESP32
  page += F("<dt>ESP32 Chip model</dt><dd>");
  page += ESP.getChipModel();
  page += F("</dd><dt>ESP32 Chip revision</dt><dd>");
  page += 'v';
  uint16_t rev = ESP.getChipRevision(); // It returns revision in the major * 100 + minor format.
  page += rev / 100;
  page += '.';
  page += rev - rev / 100 * 100;
  page += F("</dd>");
#endif
#ifdef ESP8266
  page += F("<dt>Supply voltage</dt><dd>");
  page += String(ESP.getVcc() / 1000.0, 2); // using a float and 2 decimal places
  page += F(" Volt</dd>");
#endif
  page += F("<dt>WiFi RSSI</dt><dd>");
  page += WiFi.RSSI();
  page += F(" dBm</dd>");
  page += F("</dl><hr>");
  page += F("<p/>Github <a href='https://github.com/RFD-FHEM/SIGNALDuino'>https://github.com/RFD-FHEM/SIGNALDuino</a>");
  page += FPSTR(HTTP_END);
  wifiManager.server->send(200, FPSTR(HTTP_HEAD_CT), page);
}

void bindServerCallback() {
  wifiManager.server->on("/custom", handleSignal);
  wifiManager.server->on("/favicon.ico", handleFavicon);
}

