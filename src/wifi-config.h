void saveConfigCallback() {
  DBG_PRINTLN("Should save config");
  IPAddress ip(0, 0, 0, 0);
  IPAddress gateway(0, 0, 0, 0);
  IPAddress subnet(0, 0, 0, 0);
  if (wifiManager.server->arg(FPSTR(S_ip)) != "") { // got static IP
    // Serial.println("saveConfigCallback");
    ip = WiFi.localIP();
    gateway = WiFi.gatewayIP();
    subnet = WiFi.subnetMask();
  } else { // DHCP
    DBG_PRINTLN("reset wifi static settings");
    WiFi.config(ip, gateway, subnet); // reset static ip, gw, sn
    wifiManager.disconnect();
    wifiManager.setSTAStaticIPConfig(ip, gateway, subnet); // wifimanager reset static ip, gw, sn
    bool res = wifiManager.autoConnect("AutoConnectAP", "password"); // password protected ap
    if (!res) {
      Serial.println(F("Failed to connect or hit timeout"));
      // ESP.restart();
    } else {
      wifiManager.setConfigPortalBlocking(false);
      wifiManager.startWebPortal();
    }
  }
#ifdef DEBUG
  Serial.print("IP: ");
  Serial.println(WiFi.localIP()); // WiFi.localIP() returns the local IP address assigned to the device as an IPAddress.
  Serial.print("GW: ");
  Serial.println(WiFi.gatewayIP());
  Serial.print("NM: ");
  Serial.println(WiFi.subnetMask());
#endif
  EEPROM_write_ipaddress(EE_IP, ip);
  EEPROM_write_ipaddress(EE_GW, gateway);
  EEPROM_write_ipaddress(EE_SNM, subnet);
}

String getParam(String name) {
  //read parameter from server, for customhmtl input
  String value;
  if (wifiManager.server->hasArg(name)) {
    value = wifiManager.server->arg(name);
  }
  return value;
}

void saveParamCallback() {
  //Serial.println("[CALLBACK] saveParamCallback fired");
  //Serial.println("PARAM theme = " + getParam("theme"));
  if (getParam("theme") == "1") {
    wifiManager.setClass("invert"); // set dark theme
    new (&custom_field) WiFiManagerParameter(THEME_STR[1]);
    EEPROM.write(EE_WM_THEME, 1);
  } else {
    wifiManager.setClass("");
    new (&custom_field) WiFiManagerParameter(THEME_STR[0]);
    EEPROM.write(EE_WM_THEME, 0);
  }
  EEPROM.commit();
}

void resetwifi() {
  wifiManager.resetSettings();
#ifdef esp8266
  ESP.reset();
#elif defined(ESP32)
  ESP.restart();
#endif
}

